  async function loadIntelConfig() {
    try {
      const getConfig = window.electronAPI?.getIntelligenceConfig;
      if (!getConfig) return;
      const config = await getConfig();
      if (!config) return;

      // Render weights
      const weightsEl = document.getElementById('intel-weights-config');
      if (weightsEl && config.weights) {
        weightsEl.innerHTML = Object.entries(config.weights).map(([key, val]) =>
          `<div class="config-item">
            <label>${key.replace(/_/g, ' ')}</label>
            <span class="config-value">${val}</span>
          </div>`
        ).join('');
      }

      // Render thresholds
      const threshEl = document.getElementById('intel-thresholds-config');
      if (threshEl) {
        const thresholds = {
          rapid_switch_threshold: config.rapid_switch_threshold,
          rapid_switch_window_secs: config.rapid_switch_window_secs,
          redirect_chain_threshold: config.redirect_chain_threshold,
          redirect_chain_window_secs: config.redirect_chain_window_secs,
          alert_threshold: config.alert_threshold,
          session_timeout_secs: config.session_timeout_secs
        };
        threshEl.innerHTML = Object.entries(thresholds).filter(([, v]) => v != null).map(([key, val]) =>
          `<div class="config-item">
            <label>${key.replace(/_/g, ' ')}</label>
            <span class="config-value">${val}</span>
          </div>`
        ).join('');
      }

      // Render TLDs
      const tldsEl = document.getElementById('intel-tlds-list');
      if (tldsEl && config.suspicious_tlds) {
        tldsEl.innerHTML = config.suspicious_tlds.map(tld =>
          `<span class="tld-chip">.${tld}</span>`
        ).join('');
      }
    } catch (err) {
      console.error('[IntelConfig] Load failed:', err);
    }
  }

