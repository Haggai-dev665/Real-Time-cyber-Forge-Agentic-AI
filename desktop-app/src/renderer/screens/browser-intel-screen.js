  function _browserIcon(browser) {
    if (!browser) return 'globe';
    const b = browser.toLowerCase();
    if (b.includes('chrome')) return 'chrome';
    if (b.includes('safari')) return 'safari';
    if (b.includes('firefox')) return 'firefox';
    if (b.includes('edge')) return 'edge';
    if (b.includes('brave')) return 'shield-alt';
    if (b.includes('arc')) return 'circle-notch';
    return 'globe';
  }

  function _alertIcon(reason) {
    if (!reason) return 'exclamation-triangle';
    const r = reason.toLowerCase();
    if (r.includes('rapid')) return 'bolt';
    if (r.includes('tld') || r.includes('suspicious')) return 'skull-crossbones';
    if (r.includes('redirect')) return 'random';
    if (r.includes('downgrade') || r.includes('http')) return 'unlock';
    if (r.includes('switch')) return 'exchange-alt';
    return 'exclamation-triangle';
  }

  function _formatAge(isoStr) {
    try {
      const diff = Date.now() - new Date(isoStr).getTime();
      const mins = Math.floor(diff / 60000);
      if (mins < 1) return '<1m';
      if (mins < 60) return `${mins}m`;
      const hours = Math.floor(mins / 60);
      return `${hours}h ${mins % 60}m`;
    } catch {
      return '—';
    }
  }

